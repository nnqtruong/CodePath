def find_trending_materials(brands):
    result = {}
    for brand in brands:
        for material in brand["materials"]:
            if material not in result:
                result[material]=1
            else: 
                result[material]+=1
    final =[]
    for keys,count in result.items():
        if count>1:
            final.append(material)
    return final

